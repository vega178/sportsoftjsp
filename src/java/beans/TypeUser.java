package beans;

/**
 *
 * @author santhyago
 */
public class TypeUser {
       private int id;
    private String userType;

    public TypeUser() {
    }

    public TypeUser(int id, String userType) {
        this.id = id;
        this.userType = userType;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUserType() {
        return userType;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }
    
    
}
