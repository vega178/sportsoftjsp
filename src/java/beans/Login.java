package beans;

/**
 *
 * @author santhyago
 */
public class Login {
    private int id;
    private String clave;
    private int idUser;

    public Login() {
    }

    public Login(int id, String clave, int idUser) {
        this.id = id;
        this.clave = clave;
        this.idUser = idUser;
    }

    public int getIdUser() {
        return idUser;
    }

    public void setIdUser(int idUser) {
        this.idUser = idUser;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }
    
    
    
}
